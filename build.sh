#!/bin/bash
if [ ! -n "$IRIS_INSTALL_ROOT" ]; then
	IRIS_INSTALL_ROOT="$HOME/.iris"
fi
if [ ! -n "$IRIS_TAG" ]; then
IRIS_TAG=
fi
if [ ! -n "$BUILD_DIR" ]; then
    BUILD_DIR=build
fi
BUILD_DIR_NAME=${BUILD_DIR}${IRIS_TAG}
rm -rf $BUILD_DIR_NAME
mkdir -p $BUILD_DIR_NAME
cd $BUILD_DIR_NAME
CC=gcc
CXX=g++
#FC=gfortran
